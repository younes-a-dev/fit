enum Level {
  beginner,
  intermediate,
  advanced,
}

enum WorkoutType {
  home,
  club,
  plan,
  customPlan,
}

enum PlanSessionType {
  singleSession,
  multiSession,
}

class Plan with ChangeNotifier {
  final String? id;
  final String? name;
  final String? image;
  final int? workoutTime;
  final Level? level;
  final WorkoutType? workoutType;
  final int? exerciseCount;
  bool? isFavorite;
  bool? isMain;

  // late  List<Exercise>? planExercises;
  final PlanSessionType? planSessionType;
  late final List<SessionModel>? planSessions;

  Plan({
    this.id,
    this.name,
    this.image,
    this.workoutTime,
    this.level,
    this.workoutType,
    this.exerciseCount,
    this.isFavorite = false,
    this.isMain = false,
    // this.planExercises,
    this.planSessionType,
    this.planSessions,
  });

  void toggleBookmarkStatus() {
    isFavorite = !isFavorite!;
    notifyListeners();
  }

  void toggleMainStatus() {
    isMain = !isMain!;
    notifyListeners();
  }
}

