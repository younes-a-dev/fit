  List<Plan> filterByLevelAndType(Level level, WorkoutType workoutType) {
    if (level == Level.beginner && workoutType == WorkoutType.home) {
      return items
          .where((items) =>
              items.level == Level.beginner &&
              items.workoutType == WorkoutType.home)
          .toList();
    } else if (level == Level.intermediate && workoutType == WorkoutType.home) {
      return items
          .where((items) =>
              items.level == Level.intermediate &&
              items.workoutType == WorkoutType.home)
          .toList();
    } else if (level == Level.advanced && workoutType == WorkoutType.home) {
      return items
          .where((items) =>
              items.level == Level.advanced &&
